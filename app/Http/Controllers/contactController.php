     */
    public function update(Request $request, $id)
    {
        $data = [
            'email' => $request->email,
            'phone' => $request->phone,
            'location' => $request->location,
        ];
        Contact::where('id', $id)->update($data);
        
        return redirect()->to('admin/contact')->with('success', 'Berita berhasil diperbarui');
